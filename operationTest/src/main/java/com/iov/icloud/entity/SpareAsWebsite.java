package com.iov.icloud.entity;

public class SpareAsWebsite {

    private Integer id;


    private String installWebsite;


    private String address;


    private String contacts;


    private String telephone;


    private Integer falg;


    private Integer count;


    private String region;


	public Integer getId() {
		return id;
	}


	public void setId(Integer id) {
		this.id = id;
	}


	public String getInstallWebsite() {
		return installWebsite;
	}


	public void setInstallWebsite(String installWebsite) {
		this.installWebsite = installWebsite;
	}


	public String getAddress() {
		return address;
	}


	public void setAddress(String address) {
		this.address = address;
	}


	public String getContacts() {
		return contacts;
	}


	public void setContacts(String contacts) {
		this.contacts = contacts;
	}


	public String getTelephone() {
		return telephone;
	}


	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}


	public Integer getFalg() {
		return falg;
	}


	public void setFalg(Integer falg) {
		this.falg = falg;
	}


	public Integer getCount() {
		return count;
	}


	public void setCount(Integer count) {
		this.count = count;
	}


	public String getRegion() {
		return region;
	}


	public void setRegion(String region) {
		this.region = region;
	}


   
}